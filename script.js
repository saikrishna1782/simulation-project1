// Function to validate email
function validateEmail(email) {
    var re = /\S+@\S+\.\S+/;
    return re.test(email);
}

// Function to validate phone number (10 digits without spaces)
function validatePhone(phone) {
    var re = /^\d{10}$/;
    return re.test(phone);
}

